    // List of screen sizes where the animation should NOT run
    const excludedSizes = ['mobile-1', 'mobile-2', 'mobile-3', 'mobile-4', 'mobile-5', 'mobile-5', 'mobile-6', 'mobile-7', 'mobile-8', 'mobile-9', 'mobile-10', 'mobile-11', 'mobile-12', 'mobile-14',];

