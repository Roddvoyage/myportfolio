const scrollPositions = [0, 50, 100, 150, 200]; // Adjust as needed
let animationStep = 0; // Current animation step
let canScroll = true; // Temporarily block scrolling
let currentScrollY = 0; // Track the current scroll position
let lastScrollY = 0; // Track the last scroll position

// Handle the scroll event
window.addEventListener("scroll", (event) => {
  if (!canScroll) {
    // Prevent further scrolling
    window.scrollTo(0, scrollPositions[animationStep]);
    return;
  }

  // Update scroll positions
  lastScrollY = currentScrollY;
  currentScrollY = window.scrollY;

  // Scroll Down
  if (currentScrollY > lastScrollY) {
    if (animationStep < scrollPositions.length - 1 && currentScrollY > scrollPositions[animationStep]) {
      canScroll = false; // Block scrolling
      animationStep++; // Move to the next step
      window.scrollTo(0, scrollPositions[animationStep]); // Snap to the next position

      // Trigger animations for the current step
      triggerAnimation(animationStep);

      // Re-enable scrolling after the animation completes
      setTimeout(() => {
        canScroll = true;
      }, 2000); // Adjust timing to match your animation duration
    }
  }

  // Scroll Up
  else if (currentScrollY < lastScrollY) {
    if (animationStep > 0 && currentScrollY <= scrollPositions[animationStep - 1]) {
      canScroll = false; // Block scrolling
      animationStep--; // Move to the previous step
      window.scrollTo(0, scrollPositions[animationStep]); // Snap to the previous position

      // Trigger reverse animations for the current step
      reverseAnimation(animationStep);

      // Re-enable scrolling after the animation completes
      setTimeout(() => {
        canScroll = true;
      }, 2000); // Adjust timing to match your animation duration
    }
  }
});

