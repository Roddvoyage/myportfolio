function getScreenSize() {
    const screenWidth = window.innerWidth;
    const screenHeight = window.innerHeight;

    const sizes = [
        { width: 375, height: 667, label: 'mobile-1' }, // Specific resolution
        { width: 414, height: 896, label: 'mobile-2' }, // Specific resolution
        { width: 390, height: 844, label: 'mobile-3' }, // Specific resolution
        { width: 430, height: 932, label: 'mobile-4' }, // Specific resolution
        { width: 412, height: 915, label: 'mobile-5' }, // Specific resolution
        { width: 360, height: 740, label: 'mobile-6' }, // Specific resolution
        { width: 768, height: 1024, label: 'mobile-7' }, // Specific resolution
        { width: 820, height: 1180, label: 'mobile-8' }, // Specific resolution
        { width: 1024, height: 1366, label: 'mobile-9' }, // Specific resolution
        { width: 912, height: 1368, label: 'mobile-10' }, // Specific resolution
        { width: 540, height: 720, label: 'mobile-11' }, // Specific resolution
        { width: 344, height: 882, label: 'mobile-12' }, // Specific resolution
        { width: 853, height: 1280, label: 'mobile-14' }, // Specific resolution
        { width: 412, height: 914, label: 'mobile-15' }, // Specific resolution
        { width: 1024, height: 600, label: 'mobile-16' }, // Specific resolution
        { width: 1280, height: 800, label: 'mobile-17' }, // Specific resolution



        { minWidth: 768, maxWidth: 991, label: 'small-desktop' }, // Small desktops
        { minWidth: 690, maxWidth: 767, label: 'tablet' }, // Standard tablets
        { minWidth: 480, maxWidth: 689, label: 'small-tablet' }, // Small tablets/phablets
        { maxWidth: 479, minWidth: 361, label: 'mobile' }, // General mobile devices
        { maxWidth: 360, label: 'small-mobile' }, // Very small mobile devices
        { minWidth: 1024, maxWidth: 1279, label: 'large-desktop' }, // Standard large desktops
        { minWidth: 1280, label: 'extra-large-desktop' }, // Extra-large desktops
    ];

    // Prioritize exact matches (width and height)
    for (const size of sizes) {
        if (size.width && size.height) {
            if (screenWidth === size.width && screenHeight === size.height) {
                return size.label;
            }
        }
    }

    // Check for ranges (minWidth/maxWidth)
   
    return 'unknown'; // Fallback if no size matches
}

