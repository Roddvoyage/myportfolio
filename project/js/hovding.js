  function stopAnimations(...elements) {
    elements.forEach((el) => {
      if (el) {
        el.style.animation = "none";
        el.offsetHeight; // Trigger a reflow to clear the animation
      }
    });
  }




  function restartAnimations(...elements) {
    elements.forEach((el) => {
      if (el) {
        el.style.animation = ""; // Clear the "none" style
        void el.offsetHeight; // Trigger reflow
      }
    });
  }











